SELECT U.EMAIL_USUARIO,U.APELLIDO_PATERNO,U.APELLIDO_MATERNO,U.NOMBRE,PU.PERFIL
FROM   USUARIO U,PERFIL_USUARIO PU
WHERE  U.EMAIL_USUARIO = PU.EMAIL_USUARIO
ORDER BY U.APELLIDO_PATERNO,U.APELLIDO_MATERNO,U.NOMBRE;

